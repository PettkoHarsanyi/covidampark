    public void wheelMoved(java.awt.event.MouseWheelEvent evt){
        if(evt.getWheelRotation()<0 && panelPosY<0){
            jPanel2.setLocation(0,panelPosY+= 10);
            jLabel5.setLocation(jLabel5.getLocation().x,jLabel5.getLocation().y-10);
        }
        if(evt.getWheelRotation()>0 && panelPosY>-770){
            jPanel2.setLocation(0,panelPosY-= 10);
            jLabel5.setLocation(jLabel5.getLocation().x,jLabel5.getLocation().y+10);
        }
    }
    
    public void mouseDragged(java.awt.event.MouseEvent evt){
        if(panelPosY + (evt.getY()-dragStartY) <= 0 && panelPosY + (evt.getY()-dragStartY) > -770){
            jPanel2.setLocation(0,panelPosY+= (evt.getY()-dragStartY));
            jLabel5.setLocation(jLabel5.getLocation().x,jLabel5.getLocation().y-(evt.getY()-dragStartY));

        }        
    }
    
