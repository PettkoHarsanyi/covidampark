    private void jPanel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel2MouseClicked
        int mouseX = evt.getPoint().x;
        int mouseY = evt.getPoint().y;
        if(!jPanel2.getComponentAt(mouseX,mouseY).equals(jPanel2))      // IF NOT THE PANEL IS CLICKED
        {
            JLabel component = (JLabel)jPanel2.getComponentAt(mouseX, mouseY);
            if(component instanceof JLabel && !(component.equals(moneyLabel) || component.equals(jButton5) || component.equals(jButton2) || component.equals(jButton1) )){
                JLabel label = (JLabel)jPanel2.getComponentAt(mouseX, mouseY);
                String text = label.getText();

                SelectMenu selectMenu = new SelectMenu(text,frame,evt);
                frame.getLayeredPane().add(selectMenu,5,0);
                selectMenu.requestFocusInWindow();
            }
        }
    }//GEN-LAST:event_jPanel2MouseClicked

