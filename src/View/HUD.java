    public void closeHUD(){
        Thread thread = new Thread(){
            @Override
            public void run(){
                isMoving = true;
                for(int i = 0; i < 200; i++){
                    setBounds(posX--,0,200, frame.getHeight());
                    try{
                        Thread.sleep(1);                    
                    }catch(Exception e){

                    }
                }
                isMoving = false;
            }
        };        
        if(!isMoving) thread.start();
    }
    
